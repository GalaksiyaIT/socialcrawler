package com.galaksiya.social.fetcher;

public class SocialAPIException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public SocialAPIException() {
		super();
	}

	public SocialAPIException(String message, Throwable cause) {
		super(message, cause);
	}

	public SocialAPIException(String message) {
		super(message);
	}

	public SocialAPIException(Throwable cause) {
		super(cause);
	}

}
